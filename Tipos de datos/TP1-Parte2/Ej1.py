"""1.	Calcular el perímetro y área de un rectángulo dada su base y su altura."""
#Ejercicio 1
base = 5
height = 15

perimeter = 2* (base + height)
area = base * height

print(f"El perimetro de un rectangulo de base {base} y de altura {height} es {perimeter}")
print(f"El area de un rectangulo de base {base} y de altura {height} es {area}")